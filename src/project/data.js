// Helper functions
export const getMonthNames = () => Object.keys(yearlyData.months);

export const getDaysForMonth = (month) => {
  return Object.entries(yearlyData.months[month].days)
    .map(([day, value]) => ({ day: parseInt(day), value }));
};

export const getLastNMonthsData = (n, fromMonth = null) => {
  const allMonths = getMonthNames();
  const currentMonthIndex = fromMonth 
    ? allMonths.indexOf(fromMonth)
    : new Date().getMonth();
  
  const result = [];
  for (let i = 0; i < n; i++) {
    const index = (currentMonthIndex - i + 12) % 12;
    result.unshift(allMonths[index]);
  }
  return result;
};